/* ════════════════════════════════════════════════════════════════════════
 *  3D scene frame — how logo pixels map into scene units.
 *
 *  Scene x = (px.x - CX) / PX_PER_UNIT      (centered on the mark)
 *  Scene y = (CY - px.y) / PX_PER_UNIT      (y-flip; CY picked so the whole
 *                                            bot incl. antenna is vertically
 *                                            balanced around the origin)
 * ════════════════════════════════════════════════════════════════════════ */
export const LOGO_FRAME = {
  CX: 627,
  CY: 585,
  PX_PER_UNIT: 400,
} as const;

export function logoX(px: number): number {
  return (px - LOGO_FRAME.CX) / LOGO_FRAME.PX_PER_UNIT;
}

export function logoY(px: number): number {
  return (LOGO_FRAME.CY - px) / LOGO_FRAME.PX_PER_UNIT;
}

/* ════════════════════════════════════════════════════════════════════════
 *  Chassis variants — non-uniform scales of the inner bot. The silhouette
 *  itself never deforms structurally, so every variant keeps the logo DNA.
 *  Shared so SVG + 3D variants always agree.
 * ════════════════════════════════════════════════════════════════════════ */
export interface ChassisScaleSpec {
  scaleX: number;
  scaleY: number;
}

export const CHASSIS_SCALE_SPECS: Record<string, ChassisScaleSpec> = {
  classic: { scaleX: 1.0, scaleY: 1.0 },
  square: { scaleX: 1.07, scaleY: 0.95 },
  tall: { scaleX: 0.88, scaleY: 1.06 },
  pebble: { scaleX: 1.1, scaleY: 0.9 },
  soft: { scaleX: 0.95, scaleY: 0.97 },
};

export function getChassisScale(id?: string): ChassisScaleSpec {
  return CHASSIS_SCALE_SPECS[id ?? "classic"] ?? CHASSIS_SCALE_SPECS.classic;
}
